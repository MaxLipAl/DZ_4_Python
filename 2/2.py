# Получаем количество кустов черники от пользователя
N = int(input("Введите количество кустов черники: "))

# Вводим количество ягод на каждом кусте черники и сохраняем их в списке
berries = [int(input(f"Введите количество ягод на {i + 1}-ом кусте: ")) for i in range(N)]

# Добавляем к списку ягод последние два куста, чтобы учесть двух соседей первого куста
berries += berries[:2]

# Используем max() для нахождения максимального сбора
max_collected = max(sum(berries[i:i + 3]) for i in range(N))

# Выводим результат
print("Максимальное количество собранных ягод за один заход:", max_collected)